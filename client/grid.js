function playerData(data) {
	var grid = new Array();
	var numberOfUsers = data.users.length
	var ypos = 1;
	
	// iterate for rows	
	for (var row = 0; row < numberOfUsers; row++) {
		if (data.users[row]["deaths"] == 0){
			kd_ratio = data.users[row]["kills"]
		} else {
			kd_ratio = data.users[row]["kills"]/data.users[row]["deaths"]
		}
		grid.push({ y: ypos, 
			playerText: `NAME: ${data.users[row]["userName"]}, KILLS: ${data.users[row]["kills"]}, DEATHS: ${data.users[row]["deaths"]}, K/D: ${kd_ratio}`})
			ypos += 1;
	} 
	return grid;
}

function gridData(data) {
	var grid = new Array();
	var xpos = 1; //starting xpos and ypos at 1 so the stroke will show when we make the grid below
	var ypos = 1;
	var width = 22;
	var height = 22;
	
	// iterate for rows	
	for (var row = 0; row < 40; row++) {
		grid.push( new Array() );
		
		// iterate for cells/columns inside rows
		for (var column = 0; column < 40; column++) {
            //const gridRow = 
            console.log(grid.grid);
			grid[row].push({
				x: xpos,
				y: ypos,
				width: width,
				height: height,
				click: data.grid[row][column]
			})
			// increment the x position. I.e. move it over by 50 (width variable)
			xpos += width;
		}
		// reset the x position after a row is complete
		xpos = 1;
		// increment the y position for the next row. Move it down 50 (height variable)
		ypos += height;	

	}
    data.users.forEach(element => {
        grid[element.xCordinates][element.yCordinates].click = 2 + element.userID
    }); 
	return grid;
}

